package cn.org.citycloud.zwhs.bean;

public class StoreMemberInfo {

	private long allGoods;

	private long newArrival;

	private long allOrder;

	public long getAllGoods() {
		return allGoods;
	}

	public void setAllGoods(long allGoods) {
		this.allGoods = allGoods;
	}

	public long getNewArrival() {
		return newArrival;
	}

	public void setNewArrival(long newArrival) {
		this.newArrival = newArrival;
	}

	public long getAllOrder() {
		return allOrder;
	}

	public void setAllOrder(long allOrder) {
		this.allOrder = allOrder;
	}

}
