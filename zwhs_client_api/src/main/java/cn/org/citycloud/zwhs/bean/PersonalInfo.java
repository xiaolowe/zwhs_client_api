package cn.org.citycloud.zwhs.bean;

public class PersonalInfo {

	private String wechatAliasname;

	private String memberAvatar;

	private int toPay;

	private int toReceive;

	private int toComment;

	public String getWechatAliasname() {
		return wechatAliasname;
	}

	public void setWechatAliasname(String wechatAliasname) {
		this.wechatAliasname = wechatAliasname;
	}

	public String getMemberAvatar() {
		return memberAvatar;
	}

	public void setMemberAvatar(String memberAvatar) {
		this.memberAvatar = memberAvatar;
	}

	public int getToPay() {
		return toPay;
	}

	public void setToPay(int toPay) {
		this.toPay = toPay;
	}

	public int getToReceive() {
		return toReceive;
	}

	public void setToReceive(int toReceive) {
		this.toReceive = toReceive;
	}

	public int getToComment() {
		return toComment;
	}

	public void setToComment(int toComment) {
		this.toComment = toComment;
	}

}
